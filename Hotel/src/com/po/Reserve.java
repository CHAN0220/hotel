package com.po;

public class Reserve {
	private String reserveId;
	private String reserveName;
	private String reserveIn;
	private String reserveOut;
	private String reserveRType;
	public String getReserveId() {
		return reserveId;
	}
	public void setReserveId(String reserveId) {
		this.reserveId = reserveId;
	}
	public String getReserveName() {
		return reserveName;
	}
	public void setReserveName(String reserveName) {
		this.reserveName = reserveName;
	}
	public String getReserveIn() {
		return reserveIn;
	}
	public void setReserveIn(String reserveIn) {
		this.reserveIn = reserveIn;
	}
	public String getReserveOut() {
		return reserveOut;
	}
	public void setReserveOut(String reserveOut) {
		this.reserveOut = reserveOut;
	}
	public String getReserveRType() {
		return reserveRType;
	}
	public void setReserveRType(String reserveRType) {
		this.reserveRType = reserveRType;
	}
	
}
